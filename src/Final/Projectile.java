package Final;

public class Projectile {

	private int xCoor, yCoor;

	
	public Projectile(int xCoor, int yCoor) {
		
		this.xCoor = xCoor;
		this.yCoor = yCoor;
	}

	public int getxCoor() {
		return xCoor;
	}

	public void setxCoor(int xCoor) {
		this.xCoor = xCoor;
	}

	public int getyCoor() {
		return yCoor;
	}

	public void setyCoor(int yCoor) {
		this.yCoor = yCoor;
	}
	
}
