package Final;

public class Target {

	private int xCoor;
	private int yCoor;
	
	public Target(int xCoor, int yCoor){
		this.xCoor = xCoor;
		this.yCoor = yCoor;
	}
	public int getxCoor() {
		return xCoor;
	}
	public void setxCoor(int xCoor) {
		this.xCoor = xCoor;
	}
	public int getyCoor() {
		return yCoor;
	}
	public void setyCoor(int yCoor) {
		this.yCoor = yCoor;
	}
	
	
}
